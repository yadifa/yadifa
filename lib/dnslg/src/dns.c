#include <dnscore/sys_types.h>
#include <dnscore/random.h>

static random_ctx id_random_ctx = NULL;

u16
dns_new_id(void)
{
    if(id_random_ctx == NULL)
    {
        id_random_ctx = random_init_auto();
    }
    
    u16 id = (u16)random_next(id_random_ctx);
    
    return id;
}

